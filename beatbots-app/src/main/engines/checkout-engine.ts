// Target API Checkout Engine
//
// Executes Target's checkout flow entirely via HTTP — no DOM automation.
// This is the approach used by Stellar AIO and Refract: bypass the browser
// UI and send requests directly to Target's internal cart/checkout APIs.
//
// Flow:
//   1. Consume Shape ATC cookie from pool
//   2. POST /web_checkouts/v1/cart_items  →  cart created / item added
//   3. GET  /web_checkouts/v1/cart        →  verify item in cart
//   4. POST /web_checkouts/v1/checkout    →  init checkout, get checkoutId
//   5. PUT  /web_checkouts/v1/checkout/:id/address   →  set shipping
//   6. POST /web_checkouts/v1/checkout/:id/payment   →  set payment
//   7. POST /web_checkouts/v1/checkout/:id/place_order →  place order
//
// Notes on Shape:
//   - The ATC request (step 2) requires valid Shape cookies (_abck, bm_sz, etc.)
//   - These come from the cookie pool — generated by the Shape Harvester
//   - If the pool is empty the engine will wait up to `waitForCookieTimeoutMs`
//   - On a Shape block (409/429 with Shape error body): mark cookie consumed,
//     pull a new one from pool, retry up to `shapeRetryMax` times

